import numpy as np

def add(a, b):
    return a + b

def sub(a, b):
    return a - b

def mul(a, b):
    return a * b

def div(a, b):
    return a / b

def neg(a):
    return -a

def inv(a):
    return 1/a

def sqrt(a):
    return np.sqrt(a)

def abs(a):
    return np.abs(a)

def log(a):
    return np.log(a)